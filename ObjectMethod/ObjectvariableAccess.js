let product = {
    name : "i phone 14 pro max",
    company : "Apple",
    price : 124000,
    color : "red"
}
console.log(Object.keys(product)); // get all key present in object 

console.log(Object.values(product)); // get all value present in object

console.log(Object.entries(product)); // get all key value pair

console.log(Object.entries(product).length); // get length of key value pair
